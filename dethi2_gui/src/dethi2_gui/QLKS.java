    public void themVaoTable(Phong p) {
        if(p instanceof PhongNgu) {
            model.addRow(new Object[]{p.getMaP(),p.getHangP(),p.getNgayThue(),p.getNgayTra(),p.getDonGia(),((PhongNgu) p).getSoGiuong(),"",df.format(p.tinhTienThue())});
        } else {
            PhongHop ph= (PhongHop) p;
            model.addRow(new Object[]{ph.getMaP(),ph.getHangP(),ph.getNgayThue(),ph.getNgayTra(),ph.getDonGia(),"",ph.getSoGhe(),df.format(ph.tinhTienThue())});
        }
    }
    public void xoaBang() {
        for (int i = jTable1.getRowCount(); i > 0; i--) {
            model.removeRow(i-1);
        }
    }
    public void napDuLieuChoTable() {
        xoaBang();
        for (Phong phong : dsp.arr) {
            themVaoTable(phong);
        }
    }
    public PhongNgu nhapPhongNgu() {
        String maP=txtMaP.getText();
        String hangP=txtHangP.getText();
        Date thue=chuyenChuoiThanhNgay(txtNgayThue.getText());
        Date tra=chuyenChuoiThanhNgay(txtTraNgay.getText());
        Double donGia=Double.parseDouble(txtDonGia.getText());
        int soGiuong=Integer.parseInt(txtSoGiuong.getText());
        PhongNgu pn= new PhongNgu(soGiuong, maP, hangP, thue, tra, donGia);
        return pn;
    }
    public PhongHop nhapPhongHop() {
        String maP=txtMaP.getText();
        String hangP=txtHangP.getText();
        Date thue=chuyenChuoiThanhNgay(txtNgayThue.getText());
        Date tra=chuyenChuoiThanhNgay(txtTraNgay.getText());
        Double donGia=Double.parseDouble(txtDonGia.getText());
        int soGhe=Integer.parseInt(txtSoGhe.getText());
        PhongHop ph= new PhongHop(soGhe, maP, hangP, thue, tra, donGia);
        return ph;
    }
