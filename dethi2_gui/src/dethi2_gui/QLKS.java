    public Date chuyenChuoiThanhNgay(String dts) {
        try {
            return new SimpleDateFormat("dd/MM/yy").parse(dts);
        } catch (ParseException ex) {
            Logger.getLogger(QLKS.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    private String chuyenNgayThanhChuoi(Date d) {
        return new SimpleDateFormat("dd/MM/yy").format(d);
    }
