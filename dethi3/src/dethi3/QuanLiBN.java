    private void butDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDocFileActionPerformed
        try {
            // TODO add your handling code here:
            FileReader fr = new FileReader("input.txt");
            BufferedReader br = new BufferedReader(fr);
            String line = "";
            while (true) {
                line = br.readLine();
                if (line == null) {
                    break;
                }
                String st[] = line.split(",");
                String maBN = st[0];
                String hoTen = st[1];
                Date ngayVV = chuyenChuoiThanhNgay(st[2]);
                double tienThuoc = Double.parseDouble(st[3]);
                double phiKham = Double.parseDouble(st[4]);
                double phiXN = Double.parseDouble(st[5]);
                double phiNgay = Double.parseDouble(st[6]);
                Date ngayRV = chuyenChuoiThanhNgay(st[7]);

                if (phiXN == 0) {
                  BNNoiTru noiTru=new BNNoiTru(phiNgay, ngayRV, maBN, hoTen, ngayVV, tienThuoc);
                  dsbn.themVaoDS(noiTru);

                } else {
                    BNNgoaiTru bnnt = new BNNgoaiTru(phiKham, phiXN, maBN, hoTen, ngayVV, tienThuoc);
                    dsbn.themVaoDS(bnnt);
                }
            }
            fr.close();
            br.close();
        } catch (IOException ex) {
            Logger.getLogger(QuanLiBN.class.getName()).log(Level.SEVERE, null, ex);
        }
        napDuLieuChoTable();
    }//GEN-LAST:event_butDocFileActionPerformed

