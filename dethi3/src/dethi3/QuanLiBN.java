    private void myInit() {
        dsbn = new DSBenhNhan();
        model = (DefaultTableModel) jTable1.getModel();
        jTable1.setModel(model);
    }

    private Date chuyenChuoiThanhNgay(String text) {
        try {
            return new SimpleDateFormat("dd/MM/yy").parse(text);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return null;
    }
    private String chuyenNgayThanhChuoi(Date d) {
        return new SimpleDateFormat("dd/MM/yy").format(d);
    }

    private void themVaoTable(BenhNhan bn) {
        if (bn instanceof BNNgoaiTru) {
            model.addRow(new Object[]{bn.getMa(), bn.getHoTen(), chuyenNgayThanhChuoi(bn.getNgayVao()), bn.getTienThuoc(), ((BNNgoaiTru) bn).getPhiKham(),
                ((BNNgoaiTru) bn).getPhiXetNghiem(), "", "", ((BNNgoaiTru) bn).vienPhi()});
        } else {
            BNNoiTru bnnoi = (BNNoiTru) bn;
            model.addRow(new Object[]{bnnoi.getMa(), bnnoi.getHoTen(), chuyenNgayThanhChuoi(bnnoi.getNgayVao()), bnnoi.getTienThuoc(), "", "", bnnoi.getPhiNgay(),
                chuyenNgayThanhChuoi(bnnoi.getNgayRaVien()), bnnoi.vienPhi()});
        }
    }

    public void xoaBang() {
        for (int i = jTable1.getRowCount(); i > 0; i--) {
            model.removeRow(i - 1);
        }
    }
    public void napDuLieuChoTable(){
        xoaBang();
        for (BenhNhan bn : dsbn.arr) {
            themVaoTable(bn);
        }
    }
