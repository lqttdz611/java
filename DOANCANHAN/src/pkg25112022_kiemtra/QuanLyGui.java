                fw.write(dvt.getMaXe()+"\t"+dvt.getLoaiXe()+"\t"+chuyenNgayThanhChuoi(dvt.getNgayThue())+"\t"+String.valueOf(dvt.getDonGia())+"\t"+dvt.getDieuKienBang()+"\t"+String.valueOf(((ThueTheoGio) dvt).getSoGioThue())+"\t"+""+"\t"+df.format(dvt.tinhThanhTien())+"\n");
