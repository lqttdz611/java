    private void butDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDocFileActionPerformed
        
        try {
            // TODO add your handling code here:
            FileReader fr= new FileReader("input.txt");
            BufferedReader br= new BufferedReader(fr);
            String line="";
            while(true) {
                line=br.readLine();
                if(line==null) break;
                String st[]=line.split(",");
                String maXe=st[0];
                String loaiXe=st[1];
                Date ngayChoThue=chuyenChuoiThanhNgay(st[2]);
                double donGia=Double.parseDouble(st[3]);
                String dkbl=st[4];
                double soGioThue=Double.parseDouble(st[5]);
                double soNgayThue=Double.parseDouble(st[6]);
                if(soNgayThue==0) {
                    ThueTheoGio ttg= new ThueTheoGio(soGioThue, maXe, loaiXe, ngayChoThue, donGia, dkbl);
                    dsdvct.themVaoDs(ttg);
                    themVaoTable(ttg);
                } else {
                    ThueTheoNgay ttn= new ThueTheoNgay(soNgayThue, maXe, loaiXe, ngayChoThue, donGia, dkbl);
                    dsdvct.themVaoDs(ttn);
                    themVaoTable(ttn);
                }
               
            }
            fr.close();
            br.close();
        } catch (IOException ex) {
            Logger.getLogger(QuanLyGui.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
    }//GEN-LAST:event_butDocFileActionPerformed

