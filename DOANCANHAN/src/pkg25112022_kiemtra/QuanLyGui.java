    public void themVaoTable(DichVuThue dvt) {
        if(dvt instanceof ThueTheoGio) {
            model.addRow(new Object[] {dvt.getMaXe(),dvt.getLoaiXe(),chuyenNgayThanhChuoi(dvt.getNgayThue()),dvt.getDonGia(),dvt.getDieuKienBang(),((ThueTheoGio) dvt).getSoGioThue(),"",df.format(dvt.tinhThanhTien())});
        } else {
            ThueTheoNgay ttn=(ThueTheoNgay) dvt;
            model.addRow(new Object[] {ttn.getMaXe(),ttn.getLoaiXe(),chuyenNgayThanhChuoi(ttn.getNgayThue()),dvt.getDonGia(),dvt.getDieuKienBang(),"",ttn.getSoNgayThue(),df.format(ttn.tinhThanhTien())});
        }
    }
