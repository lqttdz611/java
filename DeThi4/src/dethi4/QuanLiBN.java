    private Date chuyenChuoiThanhNgay(String nl) {
        try {
            return new SimpleDateFormat("dd/MM/yy").parse(nl);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return null;
    }

    private String chuyenNgayThanhChuoi(Date d) {
        return new SimpleDateFormat("dd/MM/yy").format(d);
    }

    public void themVaoTable(BenhNhan bn) {
        if (bn instanceof BAOHIEMYTE) {
            model.addRow(new Object[]{bn.getMaBN(), bn.getHoTen(), chuyenNgayThanhChuoi(bn.getNgayNV()), chuyenNgayThanhChuoi(bn.getNgayRV()), ((BAOHIEMYTE) bn).getMaBHYT(), "",
                chuyenBooleanThanhChuoi(bn.isPhongYC()), df.format(bn.tinhVienPhi())});
        } else {
            BAOHIEMXAHOI xh = (BAOHIEMXAHOI) bn;
            model.addRow(new Object[]{xh.getMaBN(), xh.getHoTen(), chuyenNgayThanhChuoi(xh.getNgayNV()), chuyenNgayThanhChuoi(xh.getNgayRV()), "", xh.getMaBHXH(),
                chuyenBooleanThanhChuoi(xh.isPhongYC()), df.format(xh.tinhVienPhi())});

        }
    }

