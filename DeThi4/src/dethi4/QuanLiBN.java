    private void butDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDocFileActionPerformed
        try {
            // TODO add your handling code here:
            FileReader fr = new FileReader("input.txt");
            BufferedReader br = new BufferedReader(fr);
            String line = "";
            while (true) {
                line = br.readLine();
                if (line == null) {
                    break;
                }
                String st[] = line.split(",");
                String maBN = st[0];
                String hoTen = st[1];
                Date ngayVao = chuyenChuoiThanhNgay(st[2]);
                Date ngayRa = chuyenChuoiThanhNgay(st[3]);
                String BHYT = st[4];
                String BHXH = st[5];
                boolean phongYC = (st[6].equalsIgnoreCase("0") ? false : true);
                if (BHXH.equals("0")) {
                    BAOHIEMYTE bhyt = new BAOHIEMYTE(BHYT, maBN, hoTen, ngayVao, ngayRa, phongYC);
                    dsbn.themVaoDS(bhyt);
                    themVaoTable(bhyt);
                } else {
                    BAOHIEMXAHOI bhxh = new BAOHIEMXAHOI(BHXH, maBN, hoTen, ngayVao, ngayRa, phongYC);
                    dsbn.themVaoDS(bhxh);
                    themVaoTable(bhxh);
                }
            }
            fr.close();
            br.close();
        } catch (IOException ex) {
            Logger.getLogger(QuanLiBN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_butDocFileActionPerformed

